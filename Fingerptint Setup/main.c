// Function prototypes
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_UART4_Init(void);
static void MX_UART5_Init(void);

int main(void) {
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    MX_UART4_Init();
    MX_UART5_Init();

    Open_Fingerprint_Module();
    HAL_Delay(1000);

    uint8_t finger_status = Is_Finger_Pressed();
    HAL_UART_Transmit(&huart4, &finger_status, 1, HAL_MAX_DELAY);

    while (1) {
        // Main loop
    }
}

